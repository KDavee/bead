<h1>Köszöntés</h1>

<img class="nagy2" src="./images/csip1.jpg">
<p>Az <strong>Európa Jövője Egyesület</strong> 1990. szeptember 29-én azzal a céllal alakult, hogy gyermektalálkozót szervezzen Kecskeméten, annak érdekében, hogy a kecskeméti gyerekeket megismertesse Európa gyermekeivel. Az alapötlet egyszerű volt, a kontinens országaiból gyermekeket hívnak meg, akiket kecskeméti családok látnak vendégül. A következő évben a külföldi csoportok hívják viszontlátogatásra vendéglátóikat. </p>
<p>A gyermekek és az ifjúság nemzetközi kulturális cseréjén keresztül a magyar kultúra és Kecskemét város népszerűsítését egyik fő feladatunknak tekintjük.</p>
<p>Programjainkon keresztül segítjük a határon túli magyarság helyzetének jobb megismerését, kulturális kapcsolatok kiépítését.</p>
<p>Az egyesületünk emellett aktív szereplője a városi, regionális és országos civil életnek. Bács-Kiskun megyei Önkéntes Központként működött éveken keresztül, önkénteseink mellett az iskolai közösségi szolgálat során is számos diákot foglalkoztatunk programjainkon. </p>
<p>Kiadványaikon, rendezvényeinken keresztül teszünk azért, hogy a megalakuláskor célul kitűzött eszméket szolgáljuk, így a jövő Európa polgárait, a ma gyermekeit segítsük abban, hogy előítéletektől mentes, a másságot elfogadó, toleráns nemzedékké váljanak.</p>


