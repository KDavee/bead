
<h1>A fesztiválról dióhéjban</h1>

<p>Főtéri nagyszínpad és meghitt zenei helyszínek a Romkertben, a belváros szívében. Többezres nemzetközi közönség, zenei workshopok és számtalan szabadidős tevékenység várja a résztvevőket Kecskeméten a kétévente megrendezésre kerülő Csiperó Fesztiválon. A nemzetközi gyermek- és ifjúsági találkozóra főként Európa, de a világ más országaiból is hívunk csoportokat, akik kecskeméti családoknál laknak a fesztivál ideje alatt. A következő évben a kecskeméti fiatalok utaznak vissza azokhoz, akiket vendégül láttak. Az ideérkező gyermek és ifjúsági csoportoktól azt kérjük, hogy hozzanak magukkal rájuk jellemző műsort, amit nyelvi akadályok nélkül tudnak bemutatni Kecskemét Főterén, szabadtéri színpadon. A rendezvényünk nem verseny, minden produkció egyenértékű. A fesztivál fő célkitűzései a kölcsönösség és az egyenlő esély mindenki számára.</p>
<p>Azt szeretnénk, hogy a nemzetközi programból egyetlen gyermek se maradjon ki, minden programunk ingyenes.</p>
<p>A Csiperó Fesztivál helyszíne Kecskemét, mely Magyarország közepén, a két folyó, a Duna és a Tisza között fekszik.</p>
<p>Az alföldi város történelmi főtere remek helyszíne a fesztiváloknak.</p>

